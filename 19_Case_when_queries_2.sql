-- Query 1
SELECT order_id,
       customer_id,
       order_date,
       total_amount,
       CASE 
           WHEN total_amount > 1000 THEN 'Premium'
           WHEN total_amount BETWEEN 500 AND 1000 THEN 'Standard'
           ELSE 'Basic'
       END AS customer_segment
FROM orders
WHERE order_date >= '2024-01-01'
ORDER BY total_amount DESC;

