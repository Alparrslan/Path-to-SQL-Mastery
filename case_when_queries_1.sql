-- Query 1
SELECT employee_id, 
       department_id,
       salary,
       CASE 
           WHEN salary >= 10000 THEN 'High'
           WHEN salary BETWEEN 5000 AND 9999 THEN 'Medium'
           ELSE 'Low'
       END AS salary_level
FROM employees
WHERE department_id IS NOT NULL
ORDER BY salary DESC;

